
<template>
  <h2>{{customTitle }}</h2>
  <p>{{counter}} <sup>2</sup> = {{ SquareCounter}} </p>
<p></p>
  <div>
      <button v-on:click="increase">+1   </button>
      <button  v-on:click="decrease">-1  </button>
  </div>

</template>

<script>
export default {
    props:{
        title:String,
        start:{ 
            type: Number,
            default:100,
            validator( value ){
              return value>=0
            }
        }
    },
  data(){
       return {
      counter:this.start
  }
  },
  methods:{
      getSquareValue(){
          return this.counter * this.counter;
      },
      increase(){
       this.counter++;
      },
      decrease(){
         this.counter--;
      }
  },
  computed:{
      SquareCounter(){
          return this.counter * this.counter
      },
      customTitle(){
          return this.title || 'Counter'
      }
  }
}
</script>

